#include <iostream>
#include <GL/glut.h>
#include <Windows.h>
#include "Reaction.h"


using namespace std;

int PAGE_HEIGHT = 800;
int PAGE_WIDTH = 1000;
char WorkPath[_MAX_PATH];

inline void TcharToChar(const TCHAR * tchar, char * _char)
{
	int iLength; 
	iLength = WideCharToMultiByte(CP_ACP, 0, tchar, -1, NULL, 0, NULL, NULL);  
	WideCharToMultiByte(CP_ACP, 0, tchar, -1, _char, iLength, NULL, NULL);
}

int main(int argc, char** argv)
{
	glutInit(&argc, argv);                           //初始化GLUT并处理命令行参数  
	glutInitDisplayMode(GLUT_SINGLE| GLUT_RGB);      //指定模式：单缓存OR双缓存；RGBA模式OR颜色索引模式  
	glutInitWindowSize(PAGE_WIDTH, PAGE_HEIGHT);                    //指定窗口大小（像素）  
	glutInitWindowPosition(0, 0);                //指定窗口左上角在屏幕上的位置  
	glutCreateWindow("Delineate");                      //使用OpenGL场景创建一个窗口，参数为窗口名称  
	glClearColor(1.0, 1.0, 1.0, 1.0);
//	glShadeModel(GL_FLAT);
	glutKeyboardFunc(PromptInteraction);
	glutDisplayFunc(Display);

	TCHAR work_path[_MAX_PATH] = { 0 };
	GetModuleFileName(NULL, work_path, _MAX_PATH);
	TcharToChar(work_path, WorkPath);

	glutMainLoop();
	return 0;
}